/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
char menu(const char (*option)[100],int num)
{
	/*计算横线长度*/
	int len = 0;
	for (int i = 0; i < num; i++)
		len = len > (int)strlen(option[i]) ? len : (int)strlen(option[i]);

	cout << setw(len) << setfill('-') << "" << endl;
	for (int i = 0; i < num; i++)
		cout << option[i] << endl;
	cout << setw(len) << setfill('-') << "" << endl;
	cout << setfill(' ');
	cout << "[请选择:] ";
	char c;
	while (1) {
		c = _getch();
		if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')) {
			cout << c << endl << endl;
			if (c >= 'a' && c <= 'z')
				c = c + 'A' - 'a';
			return c;
		}
	}
}